package Week1.Day1;

public class ArithmeticsOperations {

    public static void main(String[] args) {
        // Declare and initiative the variables
        int number1 = 10;
        int number2 = 20;

        // Arithmetics Opertaions
        int sum = number1 + number2; // Addition
        int difference = number2 - number1; // Subtraction
        int product = number1 * number2; // Multiplication
        int quotient = number2 / number1; // Division
        int remainder = number2 % number1; // Modulus


        // Output the results

        System.out.println("Result of Addition " + sum);
        System.out.println("Result of Subtraction " + difference);
        System.out.println("Result of Multiplication " + product);
        System.out.println("Result of Division " + quotient);
        System.out.println("Result of Modulus " + remainder);
    }
}
